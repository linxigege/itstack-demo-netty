public class NettyServer2 {


    private void bind(int port) {
        //config server nio thread group
        NioEventLoopGroup parentGroup = new NioEventLoopGroup();//NioEventLoopGroup extends MultithreadEventLoopGroup Math.max(1, SystemPropertyUitl.getInt("io.netty.eventLoopThreads",NettyRuntime.availableProcessors()*2));
        EventLoopGroup childGroup = new NioEventLoopGroup();
        try {
            ServerBootstrap b = new ServerBootstrap();
            b.group(parentGroup, childGroup)
                    .channel(NioServerSocketChannel.class)
                    .option(ChannelOption.SO_BACKLOG, 128)
                    .childHandler(new MyChannelInitializer());
            ChannelFuture f = b.bind(port).sync();
            System.out.println("netty server start done.");
            f.channel().closeFuture().sync();
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            childGroup.shutdownGracefully();
            parentGroup.shutdownGracefully();
        }

    }
}
